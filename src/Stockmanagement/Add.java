    public void sort(String str){
        if(str.equals("pname")){
            str = pname.getText();
        }
        if(str.equals("Id")){
            str = id.getText();
        }
        if(str.equals("expdate")){
            str = expdate.getText();
        }
        TableRowSorter<DefaultTableModel> tr= new TableRowSorter<DefaultTableModel>(dm);
        table.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(str));
    }
