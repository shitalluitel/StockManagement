    private void btntxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntxtActionPerformed
        try{
            Statement pst1;
            pst1=con.createStatement();
            if(btntxt.getText().equals("Add")){
                pst1.executeUpdate("insert into `details`(`ID`, `Product Name`, `Quantity`, `Price`,`Expdate`) values ('"+Integer.parseInt(id.getText())+"','"+pname.getText()+"','"+Integer.parseInt(qnty.getText())+"','"+Double.parseDouble(pric.getText())+"','"+expdate.getText()+"')");
            }
            if(btntxt.getText().equals("Delete")){
                pst1.executeUpdate("delete from `details` where `ID`= '"+Integer.parseInt(id.getText())+"'or `Product Name`='"+pname.getText()+"'");
            }
            if(btntxt.getText().equals("Update")){
                pst1.executeUpdate("update `details` set `ID`='"+Integer.parseInt(id.getText())+"',`Product Name`='"+pname.getText()+"',`Quantity`='"+Integer.parseInt(qnty.getText())+"',`Price`='"+Double.parseDouble(pric.getText())+"',`Expdate`='"+expdate.getText()+"' where `Id` = '"+id.getText()+"' or `Product Name`='"+pname.getText()+"'");
            }
            clear();
            addcolumn();
            sort("/");
        }
        catch(SQLException | NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Error "+e);
        }
        
    }//GEN-LAST:event_btntxtActionPerformed

